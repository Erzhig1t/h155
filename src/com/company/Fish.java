package com.company;

public class Fish {
    private String species;
    private int price;

    void setSpecies(String species) {
        this.species = species;
    }

    void setPrice(int price) {
        this.price = price;
    }

    String getSpecies() {
        return species;
    }

    int getPrice() {
        return price;
    }
}
